require 'spec_helper'

RSpec.describe "Frontend assets" do
  let(:root) { File.expand_path("../..", __dir__) }
  let(:public_root) { File.join(root, "public") }

  it "checks in the HTML, CSS, and JavaScript frontend files" do
    expect(File).to exist(File.join(public_root, "index.html"))
    expect(File).to exist(File.join(public_root, "styles.css"))
    expect(File).to exist(File.join(public_root, "app.js"))
    expect(File).to exist(File.join(public_root, "map_renderer.js"))
    expect(File).to exist(File.join(public_root, "assets/tilesets/original-dungeon-tileset.png"))
    expect(File).to exist(File.join(public_root, "assets/locations/village-hub.png"))
    expect(File).to exist(File.join(public_root, "assets/locations/tavern-interior.png"))
    expect(File).to exist(File.join(public_root, "assets/locations/merchant-district.png"))
    expect(File).to exist(File.join(public_root, "assets/locations/blacksmith-workshop.png"))
    expect(File).to exist(File.join(public_root, "assets/locations/armorsmith-shop.png"))
    expect(File).to exist(File.join(public_root, "assets/locations/temple-sanctuary.png"))
  end

  it "wires the frontend to the JSON API endpoints" do
    html = File.read(File.join(public_root, "index.html"))
    javascript = File.read(File.join(public_root, "app.js"))
    renderer = File.read(File.join(public_root, "map_renderer.js"))

    expect(html).to include('<link rel="stylesheet" href="/styles.css">')
    expect(html).to include('<canvas')
    expect(html).to include('id="location-art"')
    expect(html).to include('src="/assets/locations/village-hub.png"')
    expect(html).to include('id="map-canvas"')
    expect(html).to include('<script src="/map_renderer.js"></script>')
    expect(html).to include('<script src="/app.js"></script>')
    expect(javascript).to include('fetch("/games"')
    expect(javascript).to include('fetch(`/games/${this.gameId}/commands`')
    expect(javascript).to include('state.scene === "ruins" && state.dungeon?.map?.length')
    expect(javascript).to include('showCanvasMap(state.dungeon.map)')
    expect(javascript).to include('mapRows.join("\\n")')
    expect(javascript).to include('DungeonMapRenderer.create(elements.mapCanvas)')
    expect(javascript).to include('function showCanvasMap(mapRows)')
    expect(javascript).to include('function showTextMap()')
    expect(javascript).to include('function showLocationArt(scene)')
    expect(javascript).to include('const LOCATION_ARTS')
    expect(javascript).to include('"/assets/locations/village-hub.png"')
    expect(javascript).to include('"/assets/locations/tavern-interior.png"')
    expect(javascript).to include('"/assets/locations/blacksmith-workshop.png"')
    expect(javascript).to include('"/assets/locations/armorsmith-shop.png"')
    expect(javascript).to include('"/assets/locations/temple-sanctuary.png"')
    expect(javascript).to include('LOCATION_ARTS[state.scene]')
    expect(javascript).to include('"has-location-art"')
    expect(javascript).to include('function isLoggableLine(line)')
    expect(javascript).to include('function quickCommandsFor(state)')
    expect(javascript).to include('function ruinsCommands(state)')
    expect(javascript).to include('function inputModeCommand(state)')
    expect(javascript).to include('selectTab("inventory")')
    expect(javascript).to include('function selectTab(name)')
    expect(javascript).to include('state.dungeon?.nearby_loot')
    expect(javascript).to include('Cast ${firstDamageSpell.display_name}')
    expect(javascript).to include('state.pending?.confirmation')
    expect(javascript).to include('function suggestedItemCommands(player)')
    expect(javascript).to include('equippedNames.includes(item.name)')
    expect(javascript).to include('function updateCommandPlaceholder(state)')
    expect(javascript).to include('locationPanels')
    expect(javascript).to include('/^[?#.xE@]+$/')
    expect(renderer).to include('globalThis.DungeonMapRenderer')
    expect(renderer).to include('"/assets/tilesets/original-dungeon-tileset.png"')
    expect(renderer).to include('"#": "wall"')
    expect(renderer).to include('".": "floor"')
    expect(renderer).to include('" ": "floor"')
    expect(renderer).to include('"?": "fog"')
    expect(renderer).to include('"x": "player"')
    expect(renderer).to include('"E": "goblin"')
    expect(renderer).to include('"@": "lootBag"')
  end
end
